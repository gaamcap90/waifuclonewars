// Achievement system — 76 achievements across 5 categories
// Thresholds tuned for long-term progression (roughly 10× the original drafts).

export type AchievementCategory = 'combat' | 'clones' | 'arena' | 'observer' | 'secret';

export interface AchievementDef {
  id: string;
  name: string;
  description: string;     // shown when unlocked; hidden achievements show '???' until then
  points: number;
  category: AchievementCategory;
  icon: string;            // emoji icon
  hidden?: boolean;        // hides name + description until unlocked
  loreUnlockId?: string;   // lore entry to unlock when earned
  // Unlock condition — one of two forms:
  statKey?: string;        // counter achievement: unlocks when stats[statKey] >= threshold
  threshold?: number;
  eventKey?: string;       // one-shot achievement: unlocks when this event is fired
}

